class PolyMixer:
    """
    Fast polynomial color mixer with baked-in coefficients.

    This implementation does not require loading ``models/poly_model.pkl``.
    """

    def __init__(self, gray_channel_threshold: int = 3):
        self.gray_channel_threshold = int(gray_channel_threshold)

    @classmethod
    def from_cache(cls, cache_dir: str = "models") -> "PolyMixer":
        """Compatibility constructor. ``cache_dir`` is ignored in this implementation."""
        _ = cache_dir
        return cls()

    def lerp(
        self,
        r1: int,
        g1: int,
        b1: int,
        r2: int,
        g2: int,
        b2: int,
        t: float,
    ) -> Tuple[int, int, int]:
        """Mix two RGB colors using the cubic hypercubic fit."""
        t = float(np.clip(t, 0.0, 1.0))
        if t <= 0.0:
            return (r1, g1, b1)
        if t >= 1.0:
            return (r2, g2, b2)

        rgb1 = np.array([r1, g1, b1], dtype=float)
        rgb2 = np.array([r2, g2, b2], dtype=float)

        is_gray1 = (float(np.max(rgb1) - np.min(rgb1))) <= self.gray_channel_threshold
        is_gray2 = (float(np.max(rgb2) - np.min(rgb2))) <= self.gray_channel_threshold

        rgb1n = rgb1 / 255.0
        rgb2n = rgb2 / 255.0
        base = (1.0 - t) * rgb1n + t * rgb2n

        if is_gray1 and is_gray2:
            out = np.rint(np.clip(base, 0.0, 1.0) * 255.0).astype(int)
            return (int(out[0]), int(out[1]), int(out[2]))

        x = np.concatenate([rgb1n, rgb2n, [t]])
        phi = poly_features_td3(2.0 * x - 1.0)
        delta = (phi @ _BETA).reshape(3)
        y = base + t * (1.0 - t) * delta

        out = np.rint(np.clip(y, 0.0, 1.0) * 255.0).astype(int)
        return (int(out[0]), int(out[1]), int(out[2]))

    def __repr__(self) -> str:
        return f"PolyMixer(gray_channel_threshold={self.gray_channel_threshold})"
