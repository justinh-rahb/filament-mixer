"""
PolyMixer — Direct cubic hypercubic fit (no runtime sklearn model).

Implements the fixed 7D total-degree-3 polynomial approximation:
(R1, G1, B1, R2, G2, B2, t) -> (R_mix, G_mix, B_mix)

with endpoint and near-gray clamping behavior.
"""

import numpy as np
from typing import Tuple


def poly_features_td3(z: np.ndarray) -> np.ndarray:
    """Full monomial feature map up to total degree 3 for input shape (N, d)."""
    z = np.asarray(z, dtype=float)
    if z.ndim == 1:
        z = z[None, :]

    n, d = z.shape
    n_terms = (d + 3) * (d + 2) * (d + 1) // 6
    phi = np.empty((n, n_terms), dtype=float)

    idx = 0
    phi[:, idx] = 1.0
    idx += 1

    phi[:, idx:idx + d] = z
    idx += d

    for i in range(d):
        zi = z[:, i]
        for j in range(i, d):
            phi[:, idx] = zi * z[:, j]
            idx += 1

    for i in range(d):
        zi = z[:, i]
        for j in range(i, d):
            zij = zi * z[:, j]
            for k in range(j, d):
                phi[:, idx] = zij * z[:, k]
                idx += 1

    if idx != n_terms:
        raise RuntimeError(f"Feature count mismatch: built {idx}, expected {n_terms}")

    return phi


