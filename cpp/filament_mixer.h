/**
 * Mix two RGB colors using polynomial pigment mixing.
 *
 * This is a drop-in replacement for mixbox_lerp.
 *
 * @param r1,g1,b1  First color (0-255)
 * @param r2,g2,b2  Second color (0-255)
 * @param t         Mixing ratio: 0.0 = all color1, 1.0 = all color2
 * @param out_r,out_g,out_b  Output color (0-255)
 */
inline void lerp(unsigned char r1, unsigned char g1, unsigned char b1,
                 unsigned char r2, unsigned char g2, unsigned char b2,
                 float t,
                 unsigned char* out_r, unsigned char* out_g, unsigned char* out_b) {
    // Clamp t at endpoints.
    if (t <= 0.0f) {
        *out_r = r1; *out_g = g1; *out_b = b1;
        return;
    }
    if (t >= 1.0f) {
        *out_r = r2; *out_g = g2; *out_b = b2;
        return;
    }

    const int gray_thresh = 3;
    const int c1_max = std::max({static_cast<int>(r1), static_cast<int>(g1), static_cast<int>(b1)});
    const int c1_min = std::min({static_cast<int>(r1), static_cast<int>(g1), static_cast<int>(b1)});
    const int c2_max = std::max({static_cast<int>(r2), static_cast<int>(g2), static_cast<int>(b2)});
    const int c2_min = std::min({static_cast<int>(r2), static_cast<int>(g2), static_cast<int>(b2)});
    const bool is_gray1 = (c1_max - c1_min) <= gray_thresh;
    const bool is_gray2 = (c2_max - c2_min) <= gray_thresh;

    // Normalize RGB to [0,1], keep t in [0,1].
    const double td = static_cast<double>(t);
    const double rgb1n[3] = {
        static_cast<double>(r1) / 255.0,
        static_cast<double>(g1) / 255.0,
        static_cast<double>(b1) / 255.0
    };
    const double rgb2n[3] = {
        static_cast<double>(r2) / 255.0,
        static_cast<double>(g2) / 255.0,
        static_cast<double>(b2) / 255.0
    };

    const double base[3] = {
        (1.0 - td) * rgb1n[0] + td * rgb2n[0],
        (1.0 - td) * rgb1n[1] + td * rgb2n[1],
        (1.0 - td) * rgb1n[2] + td * rgb2n[2]
    };

    // Near-gray inputs use plain linear interpolation.
    if (is_gray1 && is_gray2) {
        int v0 = static_cast<int>(std::nearbyint(std::clamp(base[0], 0.0, 1.0) * 255.0));
        int v1 = static_cast<int>(std::nearbyint(std::clamp(base[1], 0.0, 1.0) * 255.0));
        int v2 = static_cast<int>(std::nearbyint(std::clamp(base[2], 0.0, 1.0) * 255.0));
        if (v0 < 0) v0 = 0; if (v0 > 255) v0 = 255;
        if (v1 < 0) v1 = 0; if (v1 > 255) v1 = 255;
        if (v2 < 0) v2 = 0; if (v2 > 255) v2 = 255;
        *out_r = static_cast<unsigned char>(v0);
        *out_g = static_cast<unsigned char>(v1);
        *out_b = static_cast<unsigned char>(v2);
        return;
    }

    // Build transformed input z = 2*x - 1.
    const double z[7] = {
        2.0 * rgb1n[0] - 1.0,
        2.0 * rgb1n[1] - 1.0,
        2.0 * rgb1n[2] - 1.0,
        2.0 * rgb2n[0] - 1.0,
        2.0 * rgb2n[1] - 1.0,
        2.0 * rgb2n[2] - 1.0,
        2.0 * td - 1.0
    };

    double features[detail_td3::TD3_FEATURES];
    detail_td3::compute_features(z, features);

    // delta = Phi @ BETA
    double delta[3] = {0.0, 0.0, 0.0};
    for (int i = 0; i < detail_td3::TD3_FEATURES; ++i) {
        delta[0] += features[i] * detail_td3::BETA[i][0];
        delta[1] += features[i] * detail_td3::BETA[i][1];
        delta[2] += features[i] * detail_td3::BETA[i][2];
    }

    // y = base + t*(1-t)*delta in normalized [0,1] space.
    const double td_scale = td * (1.0 - td);
    double y[3] = {
        base[0] + td_scale * delta[0],
        base[1] + td_scale * delta[1],
        base[2] + td_scale * delta[2]
    };

    for (int c = 0; c < 3; ++c) {
        int val = static_cast<int>(std::nearbyint(std::clamp(y[c], 0.0, 1.0) * 255.0));
        if (val < 0) val = 0;
        if (val > 255) val = 255;
        if (c == 0) *out_r = static_cast<unsigned char>(val);
        else if (c == 1) *out_g = static_cast<unsigned char>(val);
        else *out_b = static_cast<unsigned char>(val);
    }
}

/**
 * Convenience overload returning an RGB struct.
 */
inline RGB lerp(unsigned char r1, unsigned char g1, unsigned char b1,
                unsigned char r2, unsigned char g2, unsigned char b2,
                float t) {
    RGB result;
    lerp(r1, g1, b1, r2, g2, b2, t, &result.r, &result.g, &result.b);
    return result;
}

