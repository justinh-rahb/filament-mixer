inline void compute_features(const double z[TD3_INPUTS], double out[TD3_FEATURES]) {
    int idx = 0;

    // Degree 0
    out[idx++] = 1.0;

    // Degree 1
    for (int i = 0; i < TD3_INPUTS; ++i) {
        out[idx++] = z[i];
    }

    // Degree 2 (i <= j)
    for (int i = 0; i < TD3_INPUTS; ++i) {
        const double zi = z[i];
        for (int j = i; j < TD3_INPUTS; ++j) {
            out[idx++] = zi * z[j];
        }
    }

    // Degree 3 (i <= j <= k)
    for (int i = 0; i < TD3_INPUTS; ++i) {
        const double zi = z[i];
        for (int j = i; j < TD3_INPUTS; ++j) {
            const double zij = zi * z[j];
            for (int k = j; k < TD3_INPUTS; ++k) {
                out[idx++] = zij * z[k];
            }
        }
    }
}

